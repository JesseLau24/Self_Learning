# list vs dictionary:
# list is a collection of values in order
# a dictionary is a bag of values that has labels

# creat an empty dictionary:
pack = dict()

# put things in it
pack['money'] = 1200
pack['water'] = 3
pack['cat'] = 4
pack['cyber deck'] = 1
pack['battery'] = 6
pack['gun'] = 0

print(pack)

# update dictionary:
pack['money'] += 2000

# now you see 'money' is 3200:
print(pack)
print(pack['money'])




